package DataManager;

public class AddressDB {
	String phone;
	String address;
	String province;
	String district;
	String zipcode;
	
	public AddressDB(String phone, String address, String province, String district, String zipcode) {
		this.phone=phone;
		this.address=address;
		this.province=province;
		this.district=district;
		this.zipcode=zipcode;

	}
}
